class TradingAccount:
    def __init__(self, starting_capital):
        self.capital = starting_capital
        self.trades = []

    def buy(self, asset, amount):
        if amount > self.capital:
            print(f"Insufficient capital. Have ${self.capital}, need ${amount}")
