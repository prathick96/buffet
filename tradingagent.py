def build_dataframe(raw_candles, symbol, exchange_name, timeframe):
    """
    Converts raw ccxt OHLCV list into a clean, typed Pandas DataFrame.

    Raw ccxt format: [[timestamp_ms, open, high, low, close, volume], ...]
    """
    df = pd.DataFrame(
        raw_candles,
        columns=['timestamp_ms', 'open', 'high', 'low', 'close', 'volume']
    )

    # Convert millisecond timestamp → proper datetime
    df['timestamp'] = pd.to_datetime(df['timestamp_ms'], unit='ms', utc=True)

    # Add metadata columns
    df['symbol']   = symbol
    df['exchange'] = exchange_name
    df['timeframe'] = timeframe

    # Drop raw ms column
    df = df.drop(columns=['timestamp_ms'])

    # Remove duplicates (can happen at pagination boundaries)
    df = df.drop_duplicates(subset=['timestamp'])

    # Sort oldest → newest
    df = df.sort_values('timestamp').reset_index(drop=True)

    # Ensure correct data types
    for col in ['open', 'high', 'low', 'close', 'volume']:
        df[col] = pd.to_numeric(df[col], errors='coerce')

    # Drop any rows with null values
    df = df.dropna()

    return df


