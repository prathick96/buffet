import threading
import time

class SecurityWatchdog:
    """
    Independent background thread that monitors the portfolio
    and sets a HALT flag in Supabase if thresholds are breached.

    The trading loop checks this flag before every order.
    If watchdog is down → trading halts by default (fail-safe).
    """

    def __init__(self, supabase_client, circuit_breaker,
                 check_interval_seconds=30):
        self.supabase        = supabase_client
        self.breaker         = circuit_breaker
        self.interval        = check_interval_seconds
        self._running        = False
        self._thread         = None
        self._halt_flag      = False
        self._last_heartbeat = None

    def start(self):
        """Start the watchdog in a background daemon thread."""
        self._running = True
        self._thread  = threading.Thread(
            target=self._watch_loop,
            daemon=True,         # Dies when main process exits
            name='SecurityWatchdog'
        )
        self._thread.start()
        print(f"[WATCHDOG] Started — checking every {self.interval}s")

    def stop(self):
        """Gracefully stop the watchdog."""
        self._running = False
        print("[WATCHDOG] Stopped")

    def _watch_loop(self):
        """Main monitoring loop — runs in background thread."""
        while self._running:
            try:
                self._check_portfolio()
                self._check_exchange_connectivity()
                self._write_heartbeat()
                self._last_heartbeat = datetime.utcnow()
            except Exception as e:
                print(f"[WATCHDOG] Error in watch loop: {e}")
            time.sleep(self.interval)

    def _check_portfolio(self):
        """Reads latest portfolio value from Supabase, checks limits."""
        try:
            result = self.supabase.table('portfolio_snapshots')\
                .select('value, timestamp')\
                .order('timestamp', desc=True)\
                .limit(1)\
                .execute()

            if result.data:
                current_value = float(result.data[0]['value'])
                allowed       = self.breaker.check(current_value)
                if not allowed and not self._halt_flag:
                    self._set_halt(self.breaker.status['reason'])
        except Exception as e:
            # If we can't read portfolio — fail safe, set halt
            print(f"[WATCHDOG] Portfolio check failed: {e} — setting halt")
            self._set_halt("Cannot read portfolio value")

    def _check_exchange_connectivity(self):
        """Verifies exchange is reachable — halts if unreachable."""
        try:
            exchange.fetch_time()   # Lightweight ping to KuCoin
        except Exception as e:
            print(f"[WATCHDOG] Exchange unreachable: {e}")

    def _write_heartbeat(self):
        """
        Writes a heartbeat to Supabase every interval.
        Trading loop verifies this is recent — if not, it halts.
        (Protects against the watchdog itself dying silently)
        """
        try:
            self.supabase.table('watchdog_heartbeat').upsert({
                'id':        1,
                'timestamp': datetime.utcnow().isoformat(),
                'halt_flag': self._halt_flag,
                'reason':    self.breaker.status.get('reason', ''),
            }).execute()
        except Exception:
            pass   # Don't crash watchdog if Supabase write fails

    def _set_halt(self, reason: str):
        """Set the halt flag — stops all trading."""
        self._halt_flag = True
        print(f"\n[WATCHDOG] !!! HALT FLAG SET !!!")
        print(f"[WATCHDOG] Reason: {reason}")

    def can_trade(self) -> bool:
        """
        Trading loop calls this before every order.
        Returns False if halt flag is set OR watchdog heartbeat is stale.
        """
        # Check halt flag
        if self._halt_flag:
            return False

        # Check watchdog is alive (heartbeat < 2 intervals ago)
        if self._last_heartbeat:
            age = (datetime.utcnow() - self._last_heartbeat).total_seconds()
            if age > self.interval * 2:
                print(f"[WATCHDOG] Stale heartbeat ({age:.0f}s) — blocking trade")
                return False

        return True

    @property
    def status(self):
        return {
            'running':         self._running,
            'halt_flag':       self._halt_flag,
            'last_heartbeat':  self._last_heartbeat.isoformat()
                               if self._last_heartbeat else None,
            'interval':        self.interval,
        }


