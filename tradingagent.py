import pandas as pd
import pandas_ta as ta
import numpy as np

def load_ohlcv(symbol, start_date=None, end_date=None):
    query = supabase_client.table('ohlcv_data')\
        .select('timestamp,symbol,exchange,timeframe,open,high,low,close,volume')\
        .eq('symbol', symbol).order('timestamp', desc=False).limit(10000)
    if start_date:
        query = query.gte('timestamp', start_date)
    if end_date:
        query = query.lte('timestamp', end_date)
    result = query.execute()
    df = pd.DataFrame(result.data)
    df['timestamp'] = pd.to_datetime(df['timestamp'], utc=True)
    return df

def add_indicators(df):
    df = df.copy()
    for col in ['open','high','low','close','volume']:
        df[col] = pd.to_numeric(df[col], errors='coerce')
    df['rsi_14'] = ta.rsi(df['close'], length=14)
    macd = ta.macd(df['close'], fast=12, slow=26, signal=9)
    macd_cols  = macd.columns.tolist()
    df['macd']        = macd[[c for c in macd_cols if c.startswith('MACD_')][0]]
    df['macd_signal'] = macd[[c for c in macd_cols if c.startswith('MACDs_')][0]]
    df['macd_hist']   = macd[[c for c in macd_cols if c.startswith('MACDh_')][0]]
    bb = ta.bbands(df['close'], length=20, std=2)
    bb_cols = bb.columns.tolist()
    df['bb_upper']  = bb[[c for c in bb_cols if c.startswith('BBU')][0]]
    df['bb_middle'] = bb[[c for c in bb_cols if c.startswith('BBM')][0]]
    df['bb_lower']  = bb[[c for c in bb_cols if c.startswith('BBL')][0]]
    df['bb_pct']    = bb[[c for c in bb_cols if c.startswith('BBP')][0]]
    df['ema_50']        = ta.ema(df['close'], length=50)
    df['ema_200']       = ta.ema(df['close'], length=200)
    df['atr_14']        = ta.atr(df['high'], df['low'], df['close'], length=14)
    df['volume_sma_20'] = ta.sma(df['volume'], length=20)
    df['volume_ratio']  = df['volume'] / df['volume_sma_20']
    df['return_1d']     = df['close'].pct_change() * 100
    return df.dropna().reset_index(drop=True)

