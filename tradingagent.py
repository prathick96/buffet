def get_latest_timestamp(symbol):
    """Returns the most recent timestamp stored in Supabase for a symbol."""
    result = supabase_client.table('ohlcv_data')\
        .select('timestamp')\
        .eq('symbol', symbol)\
        .order('timestamp', desc=True)\
        .limit(1)\
        .execute()

    if result.data:
        return pd.Timestamp(result.data[0]['timestamp'])
    return None


def incremental_update_crypto(symbol, timeframe='1d'):
    """
    Fetches only new candles since the last stored timestamp.
    Efficient — makes 1-2 API calls instead of re-fetching years of data.
    """
    latest_ts = get_latest_timestamp(symbol)

    if latest_ts is None:
        print(f"{symbol}: No existing data — doing full 5yr fetch")
        raw = fetch_full_history(symbol, timeframe=timeframe, years=5)
    else:
        # Fetch only from last stored candle onwards
        since_ms = int(latest_ts.timestamp() * 1000)
        print(f"{symbol}: Last stored: {latest_ts.date()} — fetching new candles only")
        raw = exchange.fetch_ohlcv(symbol, timeframe=timeframe,
                                    since=since_ms, limit=500)

    if not raw:
        print(f"{symbol}: Already up to date")
        return 0

    df = build_dataframe(raw, symbol, 'kucoin', timeframe)
    inserted = insert_ohlcv_to_supabase(df)
    print(f"{symbol}: {inserted} new rows added")
    return inserted


def incremental_update_stock(ticker):
    """Fetches only recent stock data since last stored candle."""
    latest_ts = get_latest_timestamp(ticker)

    if latest_ts is None:
        print(f"{ticker}: No existing data — doing full 5yr fetch")
        df = fetch_stock_history(ticker, years=5)
    else:
        days_since = (pd.Timestamp.now(tz='UTC') - latest_ts).days + 2
        period = f"{max(days_since, 5)}d"
        print(f"{ticker}: Last stored: {latest_ts.date()} — fetching last {period}")
        stock  = yf.Ticker(ticker)
        df_raw = stock.history(period=period, interval='1d')

        if df_raw.empty:
            print(f"{ticker}: No new data")
            return 0

        df = pd.DataFrame({
            'timestamp': pd.to_datetime(df_raw.index, utc=True),
            'open':   df_raw['Open'],   'high':  df_raw['High'],
            'low':    df_raw['Low'],    'close': df_raw['Close'],
            'volume': df_raw['Volume'], 'symbol': ticker,
            'exchange': 'nasdaq',       'timeframe': '1d'
        }).reset_index(drop=True)
        df = df.dropna()

    if df is None or df.empty:
        return 0

    return insert_ohlcv_to_supabase(df)


