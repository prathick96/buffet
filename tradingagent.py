class NewsFetcher:
    """
    Fetches financial news from NewsAPI.
    Free tier: 100 requests/day, headlines only, 30 days history.
    """

    BASE_URL = "https://newsapi.org/v2/everything"

    # Keywords per asset — what to search for each symbol
    ASSET_QUERIES = {
        'BTC/USDT':  'Bitcoin OR BTC crypto price',
        'ETH/USDT':  'Ethereum OR ETH crypto',
        'SOL/USDT':  'Solana OR SOL crypto',
        'BNB/USDT':  'Binance BNB crypto',
        'AAPL':      'Apple stock AAPL earnings',
        'NVDA':      'Nvidia NVDA stock GPU AI',
        'TSLA':      'Tesla TSLA stock Elon Musk',
        'GENERAL':   'Federal Reserve interest rates inflation recession',
    }

    def __init__(self, api_key: str):
        self.api_key = api_key

    def fetch_headlines(self, symbol: str, days_back: int = 3,
                        max_articles: int = 10) -> list[dict]:
        """
        Fetches recent news headlines for a symbol.

        Returns list of {title, description, url, published_at, source}
        """
        query    = self.ASSET_QUERIES.get(symbol, symbol)
        from_dt  = (datetime.utcnow() - timedelta(days=days_back)).strftime('%Y-%m-%d')

        params = {
            'q':          query,
            'from':       from_dt,
            'sortBy':     'relevancy',
            'language':   'en',
            'pageSize':   max_articles,
            'apiKey':     self.api_key,
        }

        try:
            response = requests.get(self.BASE_URL, params=params, timeout=10)
            response.raise_for_status()
            data     = response.json()

            articles = []
            for a in data.get('articles', []):
                if a.get('title') and '[Removed]' not in a.get('title', ''):
                    articles.append({
                        'title':        a.get('title', ''),
                        'description':  a.get('description', ''),
                        'source':       a.get('source', {}).get('name', ''),
                        'published_at': a.get('publishedAt', ''),
                        'url':          a.get('url', ''),
                    })
            return articles

        except Exception as e:
            print(f"News fetch error for {symbol}: {e}")
            return []

    def fetch_all_assets(self, symbols: list, days_back: int = 2) -> dict:
        """Fetches news for all symbols + general macro news."""
        all_news = {}
        for sym in symbols + ['GENERAL']:
            articles = self.fetch_headlines(sym, days_back=days_back)
            all_news[sym] = articles
            print(f"  {sym:<12}: {len(articles)} articles")
            import time; time.sleep(0.5)   # Rate limit newsapi
        return all_news


