import uuid

class PaperPortfolio:
    """
    Simulates a brokerage account using live market prices.

    Tracks:
    - Cash balance
    - Holdings per asset (units owned)
    - Trade history
    - PnL per trade and cumulative
    - All snapshots stored in Supabase for persistence
    """

    def __init__(self, initial_capital: float, session_id: str = None,
                 commission: float = 0.001):
        self.session_id      = session_id or str(uuid.uuid4())[:8]
        self.initial_capital = initial_capital
        self.cash            = initial_capital
        self.commission      = commission
        self.holdings        = {}    # {symbol: {'units': float, 'avg_price': float}}
        self.trade_history   = []
        self.daily_snapshots = []

        print(f"Paper Portfolio initialised")
        print(f"  Session ID:      {self.session_id}")
        print(f"  Capital:         ${initial_capital:.2f}")
        print(f"  Commission:      {commission*100:.2f}%")

    def get_total_value(self, current_prices: dict) -> float:
        """Total portfolio value = cash + all holdings at current prices."""
        value = self.cash
        for sym, holding in self.holdings.items():
            price  = current_prices.get(sym, holding['avg_price'])
            value += holding['units'] * price
        return value

    def buy(self, symbol: str, price: float,
            position_size_pct: float, decision: dict) -> dict:
        """
        Executes a paper BUY order.

        position_size_pct: fraction of cash to spend (0.0 to 1.0)
        Returns trade record dict.
        """
        if position_size_pct <= 0 or self.cash < 1.0:
            return {'executed': False, 'reason': 'Insufficient cash or zero size'}

        # Already holding this asset — skip (simple no-pyramid rule)
        if symbol in self.holdings and self.holdings[symbol]['units'] > 0:
            return {'executed': False, 'reason': f'Already holding {symbol}'}

        spend        = self.cash * position_size_pct
        commission   = spend * self.commission
        net_spend    = spend - commission
        units        = net_spend / price
        cash_before  = self.cash
        self.cash   -= spend

        self.holdings[symbol] = {
            'units':     units,
            'avg_price': price,
            'entry_time': datetime.utcnow().isoformat()
        }

        trade = {
            'session_id':      self.session_id,
            'symbol':          symbol,
            'action':          'BUY',
            'position_size':   position_size_pct,
            'price':           price,
            'units':           units,
            'cash_before':     cash_before,
            'cash_after':      self.cash,
            'portfolio_value': self.cash + units * price,
            'llm_sentiment':   decision.get('llm_sentiment', ''),
            'llm_confidence':  decision.get('llm_confidence', 0),
            'rl_action':       decision.get('rl_action', ''),
            'fusion_reason':   decision.get('reasoning', ''),
            'pnl':             0.0,
            'timestamp':       datetime.utcnow().isoformat(),
        }

        self.trade_history.append(trade)
        self._log_trade_to_supabase(trade)

        print(f"  [BUY]  {symbol}: {units:.6f} units @ ${price:,.2f} "
              f"(cost: ${spend:.2f}, cash left: ${self.cash:.2f})")
        return {'executed': True, **trade}

    def sell(self, symbol: str, price: float, decision: dict) -> dict:
        """Executes a paper SELL order — closes entire position."""
        if symbol not in self.holdings or self.holdings[symbol]['units'] <= 0:
            return {'executed': False, 'reason': f'No position in {symbol}'}

        holding     = self.holdings[symbol]
        units       = holding['units']
        avg_price   = holding['avg_price']
        proceeds    = units * price
        commission  = proceeds * self.commission
        net_proceeds = proceeds - commission
        pnl         = (price - avg_price) * units - commission
        cash_before = self.cash
        self.cash  += net_proceeds

        del self.holdings[symbol]

        trade = {
            'session_id':      self.session_id,
            'symbol':          symbol,
            'action':          'SELL',
            'position_size':   1.0,
            'price':           price,
            'units':           units,
            'cash_before':     cash_before,
            'cash_after':      self.cash,
            'portfolio_value': self.cash,
            'llm_sentiment':   decision.get('llm_sentiment', ''),
            'llm_confidence':  decision.get('llm_confidence', 0),
            'rl_action':       decision.get('rl_action', ''),
            'fusion_reason':   decision.get('reasoning', ''),
            'pnl':             round(pnl, 6),
            'timestamp':       datetime.utcnow().isoformat(),
        }

        self.trade_history.append(trade)
        self._log_trade_to_supabase(trade)

        pnl_pct = (pnl / (avg_price * units)) * 100
        emoji   = '✅' if pnl > 0 else '❌'
        print(f"  [SELL] {symbol}: {units:.6f} units @ ${price:,.2f} | "
              f"PnL: ${pnl:+.4f} ({pnl_pct:+.2f}%) {emoji}")
        return {'executed': True, **trade}

    def snapshot(self, current_prices: dict):
        """Records current portfolio state to Supabase."""
        total_value    = self.get_total_value(current_prices)
        cumulative_pnl = total_value - self.initial_capital
        daily_return   = 0.0

        if self.daily_snapshots:
            prev_value   = self.daily_snapshots[-1]['total_value']
            daily_return = (total_value - prev_value) / (prev_value + 1e-8) * 100

        snap = {
            'session_id':     self.session_id,
            'timestamp':      datetime.utcnow().isoformat(),
            'cash':           round(self.cash, 4),
            'total_value':    round(total_value, 4),
            'holdings':       json.dumps({
                sym: {'units': h['units'], 'avg_price': h['avg_price']}
                for sym, h in self.holdings.items()
            }),
            'daily_return':   round(daily_return, 4),
            'cumulative_pnl': round(cumulative_pnl, 4),
        }

        self.daily_snapshots.append(snap)
        self._log_snapshot_to_supabase(snap)
        return snap

    def _log_trade_to_supabase(self, trade: dict):
        try:
            supabase_client.table('paper_trades').insert(trade).execute()
        except Exception as e:
            print(f"  [WARNING] Trade log failed: {e}")

    def _log_snapshot_to_supabase(self, snap: dict):
        try:
            supabase_client.table('paper_portfolio').insert(snap).execute()
        except Exception as e:
            print(f"  [WARNING] Snapshot log failed: {e}")

    def summary(self, current_prices: dict):
        """Prints current portfolio summary."""
        total_value    = self.get_total_value(current_prices)
        cumulative_pnl = total_value - self.initial_capital
        pnl_pct        = (cumulative_pnl / self.initial_capital) * 100
        n_trades       = len(self.trade_history)
        winning_trades = [t for t in self.trade_history
                          if t['action'] == 'SELL' and t['pnl'] > 0]
        sell_trades    = [t for t in self.trade_history if t['action'] == 'SELL']
        win_rate       = len(winning_trades) / max(len(sell_trades), 1) * 100

        print(f"\n{'='*55}")
        print(f"PAPER PORTFOLIO — Session {self.session_id}")
        print(f"{'='*55}")
        print(f"  Initial capital:  ${self.initial_capital:.2f}")
        print(f"  Current value:    ${total_value:.2f}")
        print(f"  Cumulative PnL:   ${cumulative_pnl:+.4f} ({pnl_pct:+.2f}%)")
        print(f"  Cash:             ${self.cash:.2f}")
        print(f"  Open positions:   {len(self.holdings)}")
        print(f"  Total trades:     {n_trades}")
        print(f"  Win rate:         {win_rate:.1f}%")

        if self.holdings:
            print(f"\n  Open Positions:")
            for sym, h in self.holdings.items():
                curr_price  = current_prices.get(sym, h['avg_price'])
                unrealised  = (curr_price - h['avg_price']) * h['units']
                unrealised_pct = (curr_price - h['avg_price']) / h['avg_price'] * 100
                print(f"    {sym:<12}: {h['units']:.6f} units | "
                      f"Entry: ${h['avg_price']:,.2f} | "
                      f"Now: ${curr_price:,.2f} | "
                      f"PnL: ${unrealised:+.4f} ({unrealised_pct:+.2f}%)")
        print(f"{'='*55}")

