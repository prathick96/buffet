import gymnasium as gym
from gymnasium import spaces
import numpy as np
import pandas as pd

class MultiAssetTradingEnv(gym.Env):
    """
    Multi-asset portfolio trading environment.

    The agent manages a portfolio of N assets simultaneously.
    At each step it decides how to allocate capital across assets + cash.

    Key difference from single-asset TradingEnv:
    - Action = allocation weights for each asset (continuous, not discrete)
    - Observation = indicator vectors for ALL assets stacked
    - Reward = portfolio-level Sharpe ratio (cross-asset risk management)
    """

    metadata = {'render_modes': ['human', 'silent']}

    def __init__(self, asset_dfs: dict, initial_capital=1000.0,
                 window_size=20, commission=0.001, render_mode='silent'):
        """
        asset_dfs: dict of {symbol: DataFrame_with_indicators}
                   e.g. {'BTC/USDT': btc_df, 'ETH/USDT': eth_df, 'AAPL': aapl_df}
        """
        super().__init__()

        self.assets          = list(asset_dfs.keys())
        self.dfs             = {k: v.reset_index(drop=True)
                                for k, v in asset_dfs.items()}
        self.n_assets        = len(self.assets)
        self.initial_capital = initial_capital
        self.window_size     = window_size
        self.commission      = commission
        self.render_mode     = render_mode

        # Features per asset (same as single-asset env)
        self.feature_cols = [
            'close', 'volume', 'rsi_14', 'macd', 'macd_signal',
            'macd_hist', 'bb_pct', 'ema_50', 'ema_200',
            'atr_14', 'volume_ratio', 'return_1d'
        ]
        self.n_features = len(self.feature_cols)

        # Align all DataFrames to common timestamps
        self._align_timestamps()

        # Precompute normalisation stats per asset
        self._norms = {}
        for sym, df in self.dfs.items():
            mn = df[self.feature_cols].min()
            mx = df[self.feature_cols].max()
            self._norms[sym] = {'min': mn, 'range': (mx - mn).replace(0, 1)}

        # ---------------------------------------------------------------
        # OBSERVATION SPACE
        # For each asset: window_size × n_features (flattened)
        # Plus account state: [cash_ratio, asset1_ratio, ..., assetN_ratio]
        # ---------------------------------------------------------------
        obs_per_asset = self.window_size * self.n_features
        account_size  = self.n_assets + 1   # +1 for cash ratio
        obs_total     = (obs_per_asset * self.n_assets) + account_size

        self.observation_space = spaces.Box(
            low=-np.inf, high=np.inf,
            shape=(obs_total,), dtype=np.float32
        )

        # ---------------------------------------------------------------
        # ACTION SPACE — Continuous allocation weights
        # Each value in [0, 1] represents target allocation for that asset
        # Sum > 1 is normalised to sum = 1 (remainder = cash)
        # e.g. [0.5, 0.3, 0.2] = 50% BTC, 30% ETH, 20% AAPL, 0% cash
        # e.g. [0.3, 0.1, 0.1] = 30% BTC, 10% ETH, 10% AAPL, 50% cash
        # ---------------------------------------------------------------
        self.action_space = spaces.Box(
            low=0.0, high=1.0,
            shape=(self.n_assets,), dtype=np.float32
        )

    def _align_timestamps(self):
        """
        Finds common timestamps across all assets and trims DataFrames.
        Necessary because US stocks have trading halts/weekends that
        crypto doesn't have.
        """
        # Find timestamps present in ALL asset DataFrames
        common_ts = None
        for sym, df in self.dfs.items():
            ts_set = set(df['timestamp'].dt.date.astype(str))
            if common_ts is None:
                common_ts = ts_set
            else:
                common_ts = common_ts & ts_set

        # Filter each DataFrame to common timestamps only
        for sym in self.assets:
            df = self.dfs[sym]
            mask = df['timestamp'].dt.date.astype(str).isin(common_ts)
            self.dfs[sym] = df[mask].reset_index(drop=True)

        # Use minimum length across all assets
        min_len = min(len(df) for df in self.dfs.values())
        for sym in self.assets:
            self.dfs[sym] = self.dfs[sym].iloc[:min_len].reset_index(drop=True)

        self.n_steps = min_len
        print(f"Aligned {self.n_assets} assets: {min_len} common trading days")

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)

        self.current_step  = self.window_size
        self.cash          = self.initial_capital
        self.holdings      = {sym: 0.0 for sym in self.assets}
        self.entry_prices  = {sym: 0.0 for sym in self.assets}

        self.portfolio_values = [self.initial_capital]
        self.returns          = []
        self.rebalance_count  = 0

        return self._get_observation(), {}

    def step(self, action):
        """
        action: array of shape (n_assets,) with values in [0, 1]
                representing target allocation weights
        """
        current_prices = self._get_current_prices()
        prev_value     = self._portfolio_value(current_prices)

        # Normalise action to valid allocation weights
        target_alloc = self._normalise_allocations(action)

        # Execute rebalance: buy/sell to match target allocations
        trade_costs = self._rebalance(target_alloc, current_prices)

        # Advance to next candle
        self.current_step += 1

        next_prices = self._get_current_prices()
        new_value   = self._portfolio_value(next_prices)

        step_return = (new_value - prev_value) / (prev_value + 1e-8)
        self.portfolio_values.append(new_value)
        self.returns.append(step_return)

        reward     = self._calculate_reward(step_return, trade_costs)
        terminated = self.current_step >= self.n_steps - 1
        obs        = self._get_observation()

        info = {
            'portfolio_value': new_value,
            'cash':            self.cash,
            'holdings':        dict(self.holdings),
            'prices':          current_prices,
            'step_return':     step_return,
            'rebalances':      self.rebalance_count,
        }

        return obs, reward, terminated, False, info

    def _normalise_allocations(self, action):
        """
        Converts raw action vector to valid portfolio weights.
        Remaining weight after assets goes to cash.
        """
        weights = np.clip(action, 0, 1)
        total   = weights.sum()

        # If total > 1, scale down proportionally
        if total > 1.0:
            weights = weights / total

        # Remainder is cash allocation
        cash_weight = 1.0 - weights.sum()
        return weights, cash_weight

    def _get_current_prices(self):
        step = min(self.current_step, self.n_steps - 1)
        return {sym: float(self.dfs[sym].loc[step, 'close'])
                for sym in self.assets}

    def _portfolio_value(self, prices):
        value = self.cash
        for sym in self.assets:
            value += self.holdings[sym] * prices[sym]
        return value

    def _rebalance(self, allocation_result, prices):
        """
        Rebalances portfolio to match target allocations.
        Sells over-allocated assets first, then buys under-allocated.
        Minimises unnecessary trades (reduces commission drag).
        """
        weights, cash_weight = allocation_result
        total_value  = self._portfolio_value(prices)
        total_costs  = 0.0
        tolerance    = 0.02   # Only rebalance if off by > 2%

        # Current allocations
        current_alloc = {}
        for i, sym in enumerate(self.assets):
            current_alloc[sym] = (self.holdings[sym] * prices[sym]) \
                                  / (total_value + 1e-8)

        # SELL phase: reduce over-allocated positions
        for i, sym in enumerate(self.assets):
            target = float(weights[i])
            current = current_alloc[sym]
            if current - target > tolerance:
                sell_value = (current - target) * total_value
                units_to_sell = min(sell_value / prices[sym], self.holdings[sym])
                if units_to_sell > 0:
                    cost = units_to_sell * prices[sym] * self.commission
                    self.cash    += units_to_sell * prices[sym] - cost
                    self.holdings[sym] -= units_to_sell
                    total_costs  += cost
                    self.rebalance_count += 1

        # BUY phase: increase under-allocated positions
        for i, sym in enumerate(self.assets):
            target  = float(weights[i])
            current = (self.holdings[sym] * prices[sym]) / \
                      (self._portfolio_value(prices) + 1e-8)
            if target - current > tolerance:
                buy_value = (target - current) * self._portfolio_value(prices)
                buy_value = min(buy_value, self.cash * 0.95)
                if buy_value > 0:
                    cost = buy_value * self.commission
                    units_bought = (buy_value - cost) / prices[sym]
                    self.holdings[sym] += units_bought
                    self.cash -= buy_value
                    total_costs += cost
                    self.rebalance_count += 1

        return total_costs

    def _get_observation(self):
        step  = self.current_step
        prices = self._get_current_prices()
        total_value = self._portfolio_value(prices) + 1e-8

        obs_parts = []

        # Per-asset indicator windows
        for sym in self.assets:
            df     = self.dfs[sym]
            start  = step - self.window_size
            window = df.loc[start:step-1, self.feature_cols].copy()
            mn     = self._norms[sym]['min']
            rng    = self._norms[sym]['range']
            norm   = ((window - mn) / rng).fillna(0)
            obs_parts.append(norm.values.flatten().astype(np.float32))

        # Account state: [cash_ratio, btc_ratio, eth_ratio, aapl_ratio]
        cash_ratio = self.cash / total_value
        asset_ratios = [
            (self.holdings[sym] * prices[sym]) / total_value
            for sym in self.assets
        ]
        account = np.array([cash_ratio] + asset_ratios, dtype=np.float32)
        obs_parts.append(account)

        return np.concatenate(obs_parts)

    def _calculate_reward(self, step_return, trade_costs):
        reward = step_return * 100

        if len(self.returns) >= 20:
            recent  = np.array(self.returns[-20:])
            std     = np.std(recent) + 1e-8
            sharpe  = np.mean(recent) / std
            reward += sharpe * 0.1

        reward -= trade_costs * 10

        if len(self.portfolio_values) > 1:
            peak     = max(self.portfolio_values)
            current  = self.portfolio_values[-1]
            drawdown = (peak - current) / (peak + 1e-8)
            if drawdown > 0.1:
                reward -= drawdown * 5

        return float(reward)

    def get_episode_stats(self):
        values  = np.array(self.portfolio_values)
        returns = np.array(self.returns)
        total_return = (values[-1] - values[0]) / values[0] * 100
        sharpe = float(np.mean(returns) / (np.std(returns) + 1e-8) * np.sqrt(252)) \
                 if len(returns) > 1 else 0.0
        peak   = np.maximum.accumulate(values)
        max_dd = float(np.max((peak - values) / (peak + 1e-8))) * 100
        return {
            'total_return_pct':  round(total_return, 2),
            'final_value':       round(float(values[-1]), 2),
            'max_drawdown_pct':  round(max_dd, 2),
            'sharpe_ratio':      round(sharpe, 3),
            'rebalance_count':   self.rebalance_count,
        }

