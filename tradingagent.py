# CELL 9 — Response Validator (Detects Tampered / Anomalous API Data)
#
# Protects against:
# - Exchange returning corrupted price data
# - Man-in-the-middle injecting fake prices
# - Stale data (exchange returns old prices during outage)
# - Price outliers (sudden 50% move = likely bad data, not real market)
# =============================================================================

class ResponseValidator:
    """
    Validates every API response before your agent acts on it.
    Rejects data that looks suspicious, stale, or manipulated.
    """

    # Maximum believable price move in a single daily candle
    MAX_DAILY_MOVE_PCT = {
        'BTC/USDT': 30,    # BTC rarely moves > 30% in a day
        'ETH/USDT': 40,
        'SOL/USDT': 60,    # Smaller caps can move more
        'BNB/USDT': 40,
        'AAPL':     20,    # Stocks are more constrained
        'NVDA':     25,
        'TSLA':     30,
        'default':  50,    # Fallback for unknown symbols
    }

    def __init__(self, max_data_age_seconds: int = 60):
        self.max_age   = max_data_age_seconds
        self._price_history = {}   # symbol → deque of recent prices
        self._anomaly_count = {}   # symbol → count of anomalies

    def validate_ticker(self, symbol: str, ticker: dict) -> bool:
        """
        Validates a live ticker response from an exchange.
        Returns True if data is trustworthy, False if suspicious.
        """
        issues = []

        # --- Check required fields exist ---
        required = ['last', 'high', 'low', 'timestamp']
        for field in required:
            if ticker.get(field) is None:
                issues.append(f"Missing field: {field}")

        if issues:
            self._log_anomaly(symbol, issues)
            return False

        price = float(ticker['last'])
        high  = float(ticker['high'])
        low   = float(ticker['low'])
        ts    = ticker['timestamp']

        # --- Sanity: price must be between low and high ---
        if not (low <= price <= high):
            issues.append(f"Price {price} outside [low={low}, high={high}]")

        # --- Sanity: price must be positive ---
        if price <= 0:
            issues.append(f"Non-positive price: {price}")

        # --- Staleness check ---
        if ts:
            age_seconds = (time.time() * 1000 - ts) / 1000
            if age_seconds > self.max_age:
                issues.append(f"Stale data: {age_seconds:.0f}s old (max {self.max_age}s)")

        # --- Price outlier check vs recent history ---
        if symbol in self._price_history and self._price_history[symbol]:
            last_known = self._price_history[symbol][-1]
            if last_known > 0:
                move_pct = abs(price - last_known) / last_known * 100
                max_move = self.MAX_DAILY_MOVE_PCT.get(symbol,
                           self.MAX_DAILY_MOVE_PCT['default'])
                if move_pct > max_move:
                    issues.append(f"Price outlier: {move_pct:.1f}% move "
                                  f"(max believable: {max_move}%)")

        if issues:
            self._log_anomaly(symbol, issues)
            return False

        # Record valid price in history
        if symbol not in self._price_history:
            self._price_history[symbol] = deque(maxlen=100)
        self._price_history[symbol].append(price)

        return True

    def validate_ohlcv(self, symbol: str, candle: list) -> bool:
        """
        Validates a single OHLCV candle.
        candle format: [timestamp_ms, open, high, low, close, volume]
        """
        if len(candle) < 6:
            return False

        ts, open_, high, low, close, volume = candle[:6]

        issues = []

        # Basic OHLCV constraints
        if not (low <= open_ <= high):
            issues.append(f"Open {open_} outside [low={low}, high={high}]")
        if not (low <= close <= high):
            issues.append(f"Close {close} outside [low={low}, high={high}]")
        if low > high:
            issues.append(f"Low {low} > High {high}")
        if volume < 0:
            issues.append(f"Negative volume: {volume}")
        if close <= 0 or open_ <= 0:
            issues.append(f"Non-positive price: open={open_}, close={close}")

        if issues:
            self._log_anomaly(symbol, issues)
            return False

        return True

    def _log_anomaly(self, symbol: str, issues: list):
        """Log anomaly and increment counter."""
        self._anomaly_count[symbol] = self._anomaly_count.get(symbol, 0) + 1
        print(f"[SECURITY] Data anomaly for {symbol}: {'; '.join(issues)}")
        print(f"[SECURITY] Total anomalies for {symbol}: "
              f"{self._anomaly_count[symbol]}")

    def get_anomaly_summary(self):
        return dict(self._anomaly_count)


