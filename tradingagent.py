from cryptography.fernet import Fernet
import os, json, base64, hashlib

class SecureKeyStore:
    """
    Encrypts and decrypts API keys using Fernet symmetric encryption.
    Keys are never written to disk in plain text.

    Usage:
        store = SecureKeyStore(master_password="your-strong-password")
        store.store_key("kucoin_api", "your-api-key")
        key = store.get_key("kucoin_api")
    """

    def __init__(self, master_password: str, key_file: str = '.keys.enc'):
        """
        master_password: A strong passphrase you remember (never store this)
        key_file:        Where encrypted keys are stored (safe to commit)
        """
        self.key_file = key_file
        # Derive encryption key from master password using SHA-256
        # This means the same password always generates the same key
        raw_key = hashlib.sha256(master_password.encode()).digest()
        self._fernet = Fernet(base64.urlsafe_b64encode(raw_key))
        self._keys   = self._load_store()

    def _load_store(self):
        """Load and decrypt the key store from disk."""
        if not os.path.exists(self.key_file):
            return {}
        try:
            with open(self.key_file, 'rb') as f:
                encrypted = f.read()
            decrypted = self._fernet.decrypt(encrypted)
            return json.loads(decrypted.decode())
        except Exception:
            return {}

    def _save_store(self):
        """Encrypt and save the key store to disk."""
        data      = json.dumps(self._keys).encode()
        encrypted = self._fernet.encrypt(data)
        with open(self.key_file, 'wb') as f:
            f.write(encrypted)

    def store_key(self, name: str, value: str):
        """Store an API key securely."""
        self._keys[name] = value
        self._save_store()
        print(f"Key '{name}' stored securely")

    def get_key(self, name: str) -> str:
        """Retrieve a stored API key."""
        if name not in self._keys:
            raise KeyError(f"Key '{name}' not found in secure store")
        return self._keys[name]

    def list_keys(self):
        """List stored key names (not values)."""
        return list(self._keys.keys())

    def delete_key(self, name: str):
        """Remove a key from the store."""
        if name in self._keys:
            del self._keys[name]
            self._save_store()
            print(f"Key '{name}' deleted")

    def rotate_key(self, name: str, new_value: str):
        """Replace an existing key with a new one (key rotation)."""
        old_exists = name in self._keys
        self.store_key(name, new_value)
        print(f"Key '{name}' rotated {'(replaced existing)' if old_exists else '(new key)'}")


