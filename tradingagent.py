    """
    Inserts OHLCV DataFrame into Supabase ohlcv_data table.
    Uses upsert (insert or update) so re-running won't create duplicates.

    Args:
        df:              Cleaned OHLCV DataFrame
        supabase_client: Initialized Supabase client
        batch_size:      Rows per API call (keep under 1000)
    """
    total_rows    = len(df)
    inserted_rows = 0
    errors        = 0

    # Convert DataFrame to list of dicts (Supabase expects JSON records)
    records = []
    for _, row in df.iterrows():
        records.append({
            'symbol':    row['symbol'],
            'exchange':  row['exchange'],
            'timeframe': row['timeframe'],
            'timestamp': row['timestamp'].isoformat(),
            'open':      float(row['open']),
            'high':      float(row['high']),
            'low':       float(row['low']),
            'close':     float(row['close']),
            'volume':    float(row['volume']),
        })

    # Insert in batches
    for i in range(0, total_rows, batch_size):
        batch = records[i : i + batch_size]
        batch_num = (i // batch_size) + 1
        total_batches = (total_rows // batch_size) + 1

        try:
            # upsert = insert if not exists, update if exists
            # on_conflict tells it which columns define uniqueness
            result = supabase_client.table('ohlcv_data').upsert(
                batch,
                on_conflict='symbol,exchange,timeframe,timestamp'
            ).execute()

            inserted_rows += len(batch)
            print(f"  Batch {batch_num}/{total_batches}: {len(batch)} rows inserted")

        except Exception as e:
            errors += 1
            print(f"  Batch {batch_num} FAILED: {e}")

    print(f"\nDone. {inserted_rows}/{total_rows} rows inserted. Errors: {errors}")
    return inserted_rows


