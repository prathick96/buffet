data_refresh_script = '''"""
Daily data refresh script — runs via GitHub Actions at 1 AM UTC.
Fetches latest OHLCV candles for all tracked assets.
"""
import os, sys, time, logging
from datetime import datetime

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(message)s",
    handlers=[
        logging.FileHandler(f"logs/refresh_{datetime.utcnow().strftime(\'%Y%m%d\')}.log"),
        logging.StreamHandler(sys.stdout)
    ]
)
log = logging.getLogger(__name__)

from supabase import create_client
import ccxt, pandas as pd, yfinance as yf

SUPABASE_URL = os.environ["SUPABASE_URL"]
SUPABASE_KEY = os.environ["SUPABASE_KEY"]

supabase = create_client(SUPABASE_URL, SUPABASE_KEY)
exchange = ccxt.kucoin({"enableRateLimit": True})

CRYPTO_SYMBOLS = ["BTC/USDT", "ETH/USDT", "SOL/USDT", "BNB/USDT"]
STOCK_SYMBOLS  = ["AAPL", "NVDA", "TSLA", "AMZN", "MSFT"]

def get_latest_ts(symbol):
    result = supabase.table("ohlcv_data")\\
        .select("timestamp").eq("symbol", symbol)\\
        .order("timestamp", desc=True).limit(1).execute()
    if result.data:
        return pd.Timestamp(result.data[0]["timestamp"])
    return None

def upsert_candles(records):
    for i in range(0, len(records), 500):
        batch = records[i:i+500]
        supabase.table("ohlcv_data").upsert(
            batch, on_conflict="symbol,exchange,timeframe,timestamp"
        ).execute()

def refresh_crypto():
    for sym in CRYPTO_SYMBOLS:
        try:
            latest = get_latest_ts(sym)
            since  = int(latest.timestamp() * 1000) if latest else None
            candles = exchange.fetch_ohlcv(sym, "1d", since=since, limit=10)
            if not candles:
                log.info(f"{sym}: already up to date")
                continue
            records = [{
                "symbol": sym, "exchange": "kucoin", "timeframe": "1d",
                "timestamp": pd.Timestamp(c[0], unit="ms", tz="UTC").isoformat(),
                "open": c[1], "high": c[2], "low": c[3],
                "close": c[4], "volume": c[5]
            } for c in candles]
            upsert_candles(records)
            log.info(f"{sym}: {len(records)} candles upserted")
            time.sleep(0.5)
        except Exception as e:
            log.error(f"{sym} failed: {e}")

def refresh_stocks():
    for ticker in STOCK_SYMBOLS:
        try:
            latest = get_latest_ts(ticker)
            period = "5d" if latest else "5y"
            df     = yf.Ticker(ticker).history(period=period, interval="1d")
            if df.empty:
                continue
            records = [{
                "symbol": ticker, "exchange": "nasdaq", "timeframe": "1d",
                "timestamp": pd.Timestamp(ts, tz="UTC").isoformat(),
                "open": float(row.Open), "high": float(row.High),
                "low": float(row.Low), "close": float(row.Close),
                "volume": float(row.Volume)
            } for ts, row in df.iterrows()]
            upsert_candles(records)
            log.info(f"{ticker}: {len(records)} candles upserted")
            time.sleep(0.3)
        except Exception as e:
            log.error(f"{ticker} failed: {e}")

if __name__ == "__main__":
    os.makedirs("logs", exist_ok=True)
    log.info("=== Daily Data Refresh Started ===")
    refresh_crypto()
    refresh_stocks()
    total = supabase.table("ohlcv_data").select("id", count="exact").execute()
    log.info(f"Total rows in DB: {total.count}")
    log.info("=== Refresh Complete ===")
'''

