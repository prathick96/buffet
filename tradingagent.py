import pandas as pd
import time, datetime, os

def fetch_full_history(symbol, timeframe='1d', years=5):
    """Paginated OHLCV fetch from KuCoin — same as Day 3."""
    now_ms   = exchange.milliseconds()
    since_ms = now_ms - (years * 365 * 24 * 60 * 60 * 1000)
    tf_to_ms = {'1m':60_000,'5m':300_000,'15m':900_000,
                '1h':3_600_000,'4h':14_400_000,'1d':86_400_000}
    tf_ms        = tf_to_ms.get(timeframe, 86_400_000)
    limit        = 500
    all_candles  = []
    fetch_from   = since_ms

    while fetch_from < now_ms:
        try:
            batch = exchange.fetch_ohlcv(symbol, timeframe=timeframe,
                                          since=fetch_from, limit=limit)
            if not batch:
                break
            all_candles.extend(batch)
            fetch_from = batch[-1][0] + tf_ms
            time.sleep(exchange.rateLimit / 1000)
        except Exception as e:
            print(f"Fetch error: {e}")
            break
    return all_candles

def build_dataframe(raw_candles, symbol, exchange_name, timeframe):
    """Convert raw ccxt list to clean DataFrame — same as Day 3."""
    df = pd.DataFrame(raw_candles,
                      columns=['timestamp_ms','open','high','low','close','volume'])
    df['timestamp'] = pd.to_datetime(df['timestamp_ms'], unit='ms', utc=True)
    df['symbol']    = symbol
    df['exchange']  = exchange_name
    df['timeframe'] = timeframe
    df = df.drop(columns=['timestamp_ms'])
    df = df.drop_duplicates(subset=['timestamp'])
    df = df.sort_values('timestamp').reset_index(drop=True)
    for col in ['open','high','low','close','volume']:
        df[col] = pd.to_numeric(df[col], errors='coerce')
    return df.dropna()

