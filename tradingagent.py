# CELL 7 — Rate Limit Manager (Primary Exchange Ban Prevention)
#
# Exchanges ban IPs and API keys for:
#   - Too many requests per second
#   - Repeated identical requests (bot fingerprinting)
#   - No delay between requests
#   - Predictable request intervals (too robotic)
#
# Solution: Adaptive rate limiting + jitter injection
# =============================================================================

import time
import random
import threading
from collections import deque
from datetime import datetime, timedelta

class RateLimitManager:
    """
    Adaptive rate limiter that prevents exchange bans.

    Features:
    - Per-exchange per-endpoint rate tracking
    - Automatic backoff on 429 (rate limit) responses
    - Jitter injection (randomises request timing)
    - Request budget management (daily/hourly limits)
    - Cooldown escalation (each ban doubles cooldown)
    """

    # Known rate limits per exchange (requests per second)
    EXCHANGE_LIMITS = {
        'kucoin':  {'public': 30, 'private': 10, 'order': 5},
        'binance': {'public': 20, 'private': 10, 'order': 5},
        'alpaca':  {'public': 10, 'private': 5,  'order': 3},
        'zerodha': {'public': 10, 'private': 5,  'order': 2},
    }

    def __init__(self, exchange_name: str):
        self.exchange      = exchange_name.lower()
        self.limits        = self.EXCHANGE_LIMITS.get(self.exchange, {'public':5,'private':3,'order':2})
        self._windows      = {}      # Sliding window per endpoint type
        self._lock         = threading.Lock()
        self._cooldowns    = {}      # Active cooldowns per endpoint
        self._backoff_mult = {}      # Escalation multiplier per endpoint
        self._request_log  = deque(maxlen=1000)   # Audit log

    def wait_if_needed(self, endpoint_type: str = 'public'):
        """
        Call this BEFORE every API request.
        Blocks until it's safe to make the request.

        endpoint_type: 'public' (price data), 'private' (account), 'order' (trades)
        """
        with self._lock:
            # Check if in cooldown
            if endpoint_type in self._cooldowns:
                cooldown_until = self._cooldowns[endpoint_type]
                remaining = (cooldown_until - datetime.utcnow()).total_seconds()
                if remaining > 0:
                    print(f"[SECURITY] Rate limit cooldown: waiting {remaining:.1f}s for {endpoint_type}")
                    time.sleep(remaining + random.uniform(0.5, 2.0))  # Extra jitter
                else:
                    del self._cooldowns[endpoint_type]

            # Sliding window rate check
            limit_rps = self.limits.get(endpoint_type, 5)
            window_key = f"{self.exchange}_{endpoint_type}"

            if window_key not in self._windows:
                self._windows[window_key] = deque()

            window = self._windows[window_key]
            now    = time.time()

            # Remove requests older than 1 second from window
            while window and now - window[0] > 1.0:
                window.popleft()

            # If at limit, wait
            if len(window) >= limit_rps:
                sleep_time = 1.0 - (now - window[0])
                if sleep_time > 0:
                    time.sleep(sleep_time)

            # Inject jitter (prevents robotic fingerprinting)
            # Random delay between 50ms and 200ms
            jitter = random.uniform(0.05, 0.20)
            time.sleep(jitter)

            # Record this request
            window.append(time.time())
            self._request_log.append({
                'time':     datetime.utcnow().isoformat(),
                'exchange': self.exchange,
                'endpoint': endpoint_type,
            })

    def report_rate_limit_error(self, endpoint_type: str = 'public'):
        """
        Call this when you receive a 429 or rate limit error.
        Escalates cooldown with each successive ban.
        """
        mult = self._backoff_mult.get(endpoint_type, 1)
        cooldown_seconds = 60 * mult  # Start at 60s, doubles each time

        print(f"[SECURITY] Rate limit hit on {self.exchange}/{endpoint_type}. "
              f"Cooldown: {cooldown_seconds}s (multiplier: {mult}x)")

        self._cooldowns[endpoint_type] = datetime.utcnow() + \
                                          timedelta(seconds=cooldown_seconds)
        self._backoff_mult[endpoint_type] = min(mult * 2, 32)  # Cap at 32x = 32min

    def reset_backoff(self, endpoint_type: str):
        """Call after a successful period — resets the escalation multiplier."""
        if endpoint_type in self._backoff_mult:
            self._backoff_mult[endpoint_type] = 1
            print(f"[SECURITY] Backoff reset for {self.exchange}/{endpoint_type}")

    def get_request_stats(self):
        """Returns request count stats from the audit log."""
        if not self._request_log:
            return {}
        recent = [r for r in self._request_log
                  if (datetime.utcnow() -
                      datetime.fromisoformat(r['time'])).total_seconds() < 3600]
        return {
            'requests_last_hour':  len(recent),
            'requests_last_minute': sum(1 for r in recent
                if (datetime.utcnow() -
                    datetime.fromisoformat(r['time'])).total_seconds() < 60),
        }

