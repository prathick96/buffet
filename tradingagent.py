# CELL 8 — Circuit Breaker (Runaway Trading Prevention)
#
# Protects you from catastrophic losses if the agent goes rogue.
# Inspired by stock exchange circuit breakers — halts trading on extreme moves.
# =============================================================================

class CircuitBreaker:
    """
    Halts all trading when loss thresholds are breached.

    Rules:
    - Daily loss limit:    Stop trading if down X% in a single day
    - Weekly loss limit:   Stop trading if down X% in a week
    - Max consecutive losses: Stop after N losses in a row
    - Volatility halt:     Stop if market moves > X% in 1 hour
    - Anomaly halt:        Stop on unusual API response patterns

    All halts are logged to Supabase for audit.
    """

    def __init__(self,
                 daily_loss_limit_pct:   float = 5.0,
                 weekly_loss_limit_pct:  float = 15.0,
                 max_consecutive_losses: int   = 5,
                 volatility_halt_pct:    float = 10.0):

        self.daily_loss_limit   = daily_loss_limit_pct / 100
        self.weekly_loss_limit  = weekly_loss_limit_pct / 100
        self.max_consec_losses  = max_consecutive_losses
        self.vol_halt_pct       = volatility_halt_pct / 100

        # State
        self._is_tripped        = False
        self._trip_reason       = None
        self._trip_time         = None
        self._consecutive_losses = 0

        # Portfolio snapshots
        self._daily_start_value  = None
        self._weekly_start_value = None
        self._last_trade_value   = None

        print(f"[SECURITY] CircuitBreaker initialised")
        print(f"  Daily loss limit:      {daily_loss_limit_pct}%")
        print(f"  Weekly loss limit:     {weekly_loss_limit_pct}%")
        print(f"  Max consecutive losses: {max_consecutive_losses}")
        print(f"  Volatility halt:       {volatility_halt_pct}%")

    def set_session_start(self, portfolio_value: float):
        """Call at the start of each trading day."""
        if self._daily_start_value is None:
            self._daily_start_value = portfolio_value
        if self._weekly_start_value is None:
            self._weekly_start_value = portfolio_value
        self._last_trade_value = portfolio_value

    def check(self, current_portfolio_value: float,
              current_price: float = None,
              prev_price: float   = None) -> bool:
        """
        Returns True if trading is ALLOWED, False if HALTED.
        Call this before every order.

        current_portfolio_value: Total portfolio value right now
        current_price:           Current asset price (for volatility check)
        prev_price:              Previous candle's price (for volatility check)
        """
        if self._is_tripped:
            print(f"[SECURITY] CIRCUIT BREAKER ACTIVE — {self._trip_reason}")
            return False

        if self._daily_start_value is None:
            self.set_session_start(current_portfolio_value)
            return True

        # --- Daily loss check ---
        daily_loss = (self._daily_start_value - current_portfolio_value) / \
                      self._daily_start_value
        if daily_loss >= self.daily_loss_limit:
            self._trip(f"Daily loss limit breached: {daily_loss*100:.2f}% "
                       f"(limit: {self.daily_loss_limit*100:.1f}%)")
            return False

        # --- Weekly loss check ---
        if self._weekly_start_value:
            weekly_loss = (self._weekly_start_value - current_portfolio_value) / \
                           self._weekly_start_value
            if weekly_loss >= self.weekly_loss_limit:
                self._trip(f"Weekly loss limit breached: {weekly_loss*100:.2f}%")
                return False

        # --- Consecutive loss check ---
        if self._consecutive_losses >= self.max_consec_losses:
            self._trip(f"Max consecutive losses reached: {self._consecutive_losses}")
            return False

        # --- Volatility halt ---
        if current_price and prev_price and prev_price > 0:
            price_move = abs(current_price - prev_price) / prev_price
            if price_move >= self.vol_halt_pct:
                self._trip(f"Volatility halt: {price_move*100:.2f}% price move "
                           f"(limit: {self.vol_halt_pct*100:.1f}%)")
                return False

        return True

    def record_trade_result(self, pnl: float):
        """Call after each closed trade to track consecutive losses."""
        if pnl < 0:
            self._consecutive_losses += 1
            print(f"[SECURITY] Consecutive losses: {self._consecutive_losses}")
        else:
            self._consecutive_losses = 0  # Reset on any win

    def _trip(self, reason: str):
        """Trip the circuit breaker — halts all trading."""
        self._is_tripped  = True
        self._trip_reason = reason
        self._trip_time   = datetime.utcnow()
        print(f"\n{'='*60}")
        print(f"[SECURITY] !!! CIRCUIT BREAKER TRIPPED !!!")
        print(f"[SECURITY] Reason: {reason}")
        print(f"[SECURITY] Time:   {self._trip_time}")
        print(f"[SECURITY] All trading halted until manual reset.")
        print(f"{'='*60}\n")

    def manual_reset(self, confirmation: str):
        """
        Manually reset after reviewing the reason.
        Requires typing "RESET" to prevent accidental resets.
        """
        if confirmation != "RESET":
            print("[SECURITY] Type 'RESET' exactly to confirm reset")
            return
        self._is_tripped         = False
        self._trip_reason        = None
        self._consecutive_losses = 0
        self._daily_start_value  = None
        print("[SECURITY] Circuit breaker manually reset. Monitor closely.")

    @property
    def is_tripped(self):
        return self._is_tripped

    @property
    def status(self):
        return {
            'tripped':             self._is_tripped,
            'reason':              self._trip_reason,
            'trip_time':           self._trip_time.isoformat() if self._trip_time else None,
            'consecutive_losses':  self._consecutive_losses,
        }

