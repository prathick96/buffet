from stable_baselines3 import PPO
from stable_baselines3.common.vec_env import DummyVecEnv, VecNormalize
import numpy as np

class FusionEngine:
    """
    The Fusion Engine is the final decision layer.
    It arbitrates between the LLM signal and RL action,
    then passes the result to the Security Agent before execution.

    Decision matrix:
    ┌─────────────┬──────────┬─────────────────────────────────────┐
    │ LLM Signal  │ RL Action│ Fusion Decision                     │
    ├─────────────┼──────────┼─────────────────────────────────────┤
    │ BULLISH>0.7 │ BUY      │ EXECUTE FULL — both agree strongly  │
    │ BULLISH>0.7 │ HOLD     │ EXECUTE HALF — LLM leads            │
    │ BULLISH>0.7 │ SELL     │ HOLD — conflict, wait for clarity   │
    │ NEUTRAL     │ BUY      │ EXECUTE HALF — RL leads, caution    │
    │ NEUTRAL     │ HOLD     │ HOLD                                │
    │ NEUTRAL     │ SELL     │ EXECUTE HALF SELL                   │
    │ BEARISH>0.7 │ SELL     │ EXECUTE FULL — both agree           │
    │ BEARISH>0.7 │ HOLD     │ EXECUTE HALF SELL                   │
    │ BEARISH>0.7 │ BUY      │ HOLD — conflict, wait              │
    │ HIGH RISK   │ ANY      │ REDUCE ALL POSITIONS 50%            │
    └─────────────┴──────────┴─────────────────────────────────────┘
    """

    # Map RL discrete actions to labels
    RL_ACTION_MAP = {0: 'HOLD', 1: 'BUY', 2: 'SELL'}

    # Position sizes for each decision
    POSITION_SIZES = {
        'EXECUTE_FULL':       1.0,    # Use full available capital
        'EXECUTE_HALF':       0.5,    # Use 50% of available capital
        'EXECUTE_QUARTER':    0.25,   # Use 25% of available capital
        'HOLD':               0.0,    # Do nothing
        'REDUCE_HALF':       -0.5,    # Sell 50% of current position
        'CLOSE_ALL':         -1.0,    # Sell entire position
    }

    def __init__(self, security_agent, min_llm_confidence: float = 0.6):
        self.security             = security_agent
        self.min_llm_confidence   = min_llm_confidence
        self._decision_log        = []

    def decide(self, symbol: str, llm_signal: dict,
               rl_action_raw: int, current_portfolio_value: float,
               current_price: float = None,
               prev_price: float    = None) -> dict:
        """
        Makes the final trading decision by fusing LLM + RL signals.

        Returns a decision dict:
        {
            action:        'BUY' | 'SELL' | 'HOLD',
            position_size:  float (fraction of capital to use),
            reasoning:      str,
            blocked:        bool (True if security agent blocked it),
            llm_sentiment:  str,
            rl_action:      str,
        }
        """
        # Step 1: Security gate — check if trading is allowed at all
        if not self.security.can_trade(
            current_portfolio_value=current_portfolio_value,
            current_price=current_price,
            prev_price=prev_price
        ):
            return self._decision(
                symbol=symbol, action='HOLD', size=0.0,
                reason='Security agent blocked: circuit breaker or rate limit',
                blocked=True, llm=llm_signal, rl=rl_action_raw
            )

        # Step 2: Parse inputs
        llm_sentiment  = llm_signal.get('sentiment',        'NEUTRAL')
        llm_confidence = float(llm_signal.get('confidence', 0.0))
        llm_action     = llm_signal.get('suggested_action', 'HOLD')
        llm_risk       = llm_signal.get('risk_level',       'MEDIUM')
        rl_action_str  = self.RL_ACTION_MAP.get(rl_action_raw, 'HOLD')

        # Step 3: High risk override — always reduce risk when LLM flags HIGH
        if llm_risk == 'HIGH' and llm_signal.get('source') != 'llm_agent_fallback':
            return self._decision(
                symbol=symbol, action='SELL', size=0.5,
                reason=f'High risk: {llm_signal.get("reasoning","")}',
                blocked=False, llm=llm_signal, rl=rl_action_raw
        )

        # Step 4: Low LLM confidence — defer entirely to RL
        if llm_confidence < self.min_llm_confidence:
            size = 0.5 if rl_action_str != 'HOLD' else 0.0
            return self._decision(
                symbol=symbol, action=rl_action_str, size=size,
                reason=f'LLM confidence {llm_confidence:.2f} < threshold '
                       f'{self.min_llm_confidence} — deferring to RL',
                blocked=False, llm=llm_signal, rl=rl_action_raw
            )

        # Step 5: Apply decision matrix
        bullish = llm_sentiment == 'BULLISH' and llm_confidence >= 0.7
        bearish = llm_sentiment == 'BEARISH' and llm_confidence >= 0.7

        if bullish and rl_action_str == 'BUY':
            action, size, reason = 'BUY', 1.0, 'Strong agreement: LLM bullish + RL BUY'

        elif bullish and rl_action_str == 'HOLD':
            action, size, reason = 'BUY', 0.5, 'LLM bullish, RL neutral — half position'

        elif bullish and rl_action_str == 'SELL':
            action, size, reason = 'HOLD', 0.0, 'Conflict: LLM bullish vs RL SELL — waiting'

        elif bearish and rl_action_str == 'SELL':
            action, size, reason = 'SELL', 1.0, 'Strong agreement: LLM bearish + RL SELL'

        elif bearish and rl_action_str == 'HOLD':
            action, size, reason = 'SELL', 0.5, 'LLM bearish, RL neutral — half exit'

        elif bearish and rl_action_str == 'BUY':
            action, size, reason = 'HOLD', 0.0, 'Conflict: LLM bearish vs RL BUY — waiting'

        elif llm_sentiment == 'NEUTRAL' and rl_action_str == 'BUY':
            action, size, reason = 'BUY', 0.5, 'LLM neutral, RL BUY — cautious entry'

        elif llm_sentiment == 'NEUTRAL' and rl_action_str == 'SELL':
            action, size, reason = 'SELL', 0.5, 'LLM neutral, RL SELL — cautious exit'

        else:
            action, size, reason = 'HOLD', 0.0, 'Both signals neutral — holding'

        return self._decision(
            symbol=symbol, action=action, size=size, reason=reason,
            blocked=False, llm=llm_signal, rl=rl_action_raw
        )

    def _decision(self, symbol, action, size, reason,
                  blocked, llm, rl) -> dict:
        """Builds and logs a decision dict."""
        decision = {
            'symbol':         symbol,
            'action':         action,
            'position_size':  size,
            'reasoning':      reason,
            'blocked':        blocked,
            'llm_sentiment':  llm.get('sentiment', 'N/A'),
            'llm_confidence': llm.get('confidence', 0.0),
            'rl_action':      self.RL_ACTION_MAP.get(rl, 'HOLD'),
            'timestamp':      datetime.utcnow().isoformat(),
        }
        self._decision_log.append(decision)
        self._print_decision(decision)
        return decision

    def _print_decision(self, d: dict):
        """Prints a formatted decision summary."""
        status = '🚫 BLOCKED' if d['blocked'] else f"✅ {d['action']}"
        print(f"\n[FUSION] {d['symbol']} → {status} "
              f"(size: {d['position_size']*100:.0f}%)")
        print(f"  LLM: {d['llm_sentiment']} ({d['llm_confidence']:.2f}) | "
              f"RL: {d['rl_action']}")
        print(f"  Reason: {d['reasoning']}")

    def get_decision_log(self) -> pd.DataFrame:
        """Returns full decision history as DataFrame."""
        return pd.DataFrame(self._decision_log)

