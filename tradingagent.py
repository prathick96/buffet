# CELL 3 — The Core Problem: Fetching 5 Years of Data
#
# Most exchanges return max 500-1500 candles per API call.
# 5 years of daily candles = 1825 rows.
# Solution: Paginate — fetch in chunks, moving backwards in time.
# =============================================================================

import ccxt
import pandas as pd
import time
import datetime

exchange = ccxt.kucoin({'enableRateLimit': True})

def fetch_full_history(symbol, timeframe='1d', years=5):
    """
    Fetches complete historical OHLCV data by paginating backwards.

    Why paginate: KuCoin returns max 1500 candles per call.
    5 years daily = 1825 candles → needs 2 calls minimum.
    We loop until we've collected everything.

    Args:
        symbol:    Trading pair e.g. 'BTC/USDT'
        timeframe: Candle size — '1d', '4h', '1h'
        years:     How many years back to fetch

    Returns:
        Cleaned Pandas DataFrame sorted oldest → newest
    """

    # Calculate how far back to go (in milliseconds)
    now_ms    = exchange.milliseconds()
    years_ms  = years * 365 * 24 * 60 * 60 * 1000
    since_ms  = now_ms - years_ms

    # Timeframe → milliseconds (for pagination step calculation)
    tf_to_ms = {
        '1m':  60_000,
        '5m':  300_000,
        '15m': 900_000,
        '1h':  3_600_000,
        '4h':  14_400_000,
        '1d':  86_400_000,
    }
    tf_ms  = tf_to_ms.get(timeframe, 86_400_000)
    limit  = 500   # Conservative batch size — works on all exchanges
    all_candles = []
    fetch_from  = since_ms
    call_count  = 0

    print(f"Fetching {symbol} {timeframe} from {years} years ago...")
    print(f"Start date: {datetime.datetime.utcfromtimestamp(since_ms/1000).date()}")
    print(f"End date:   {datetime.datetime.utcnow().date()}")
    print(f"Estimated candles: ~{years * 365} | Batch size: {limit}")
    print()

    while fetch_from < now_ms:
        try:
            batch = exchange.fetch_ohlcv(
                symbol,
                timeframe=timeframe,
                since=fetch_from,
                limit=limit
            )

            if not batch:
                break   # No more data available

            all_candles.extend(batch)
            call_count += 1

            # Move forward: last candle timestamp + 1 timeframe step
            last_ts   = batch[-1][0]
            fetch_from = last_ts + tf_ms

            # Progress indicator
            progress_date = datetime.datetime.utcfromtimestamp(last_ts/1000).date()
            print(f"  Batch {call_count:>2}: fetched {len(batch)} candles | up to {progress_date}")

            # Respect rate limits — KuCoin allows 30 req/sec public
            time.sleep(exchange.rateLimit / 1000)

        except ccxt.RateLimitExceeded:
            print("Rate limit hit — waiting 10 seconds...")
            time.sleep(10)
        except Exception as e:
            print(f"Error at batch {call_count}: {e}")
            break

    print(f"\nTotal fetched: {len(all_candles)} candles across {call_count} API calls")
    return all_candles


