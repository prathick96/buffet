import plotly.graph_objects as go
from plotly.subplots import make_subplots

def load_paper_performance(session_id: str) -> tuple[pd.DataFrame, pd.DataFrame]:
    """Loads paper trade history and portfolio snapshots from Supabase."""
    trades = supabase_client.table('paper_trades')\
        .select('*').eq('session_id', session_id)\
        .order('timestamp', desc=False).execute()
    portfolio = supabase_client.table('paper_portfolio')\
        .select('*').eq('session_id', session_id)\
        .order('timestamp', desc=False).execute()
    trades_df    = pd.DataFrame(trades.data)
    portfolio_df = pd.DataFrame(portfolio.data)
    if not portfolio_df.empty:
        portfolio_df['timestamp']  = pd.to_datetime(portfolio_df['timestamp'])
        portfolio_df['total_value'] = pd.to_numeric(
            portfolio_df['total_value'], errors='coerce'
        )
    return trades_df, portfolio_df


def build_dashboard(session_id: str, initial_capital: float = 20.0):
    """
    Builds an interactive Plotly dashboard showing:
    - Portfolio value over time
    - PnL per trade (waterfall)
    - Win/loss distribution
    - Decision breakdown (LLM vs RL vs Fusion)
    """
    trades_df, portfolio_df = load_paper_performance(session_id)

    if portfolio_df.empty:
        print(f"No data for session {session_id} yet.")
        print("Run at least one paper trading cycle first.")
        return

    fig = make_subplots(
        rows=2, cols=2,
        subplot_titles=(
            'Portfolio Value Over Time',
            'Cumulative PnL',
            'Trade PnL Distribution',
            'Decisions by Agent'
        ),
        specs=[
            [{'type': 'scatter'}, {'type': 'scatter'}],
            [{'type': 'bar'},     {'type': 'pie'}]
        ]
    )

    dark_bg   = '#0d1117'
    panel_bg  = '#161b22'
    text_col  = '#e6edf3'
    green     = '#2ea043'
    red       = '#f85149'
    blue      = '#378ADD'

    # Panel 1: Portfolio value
    fig.add_trace(go.Scatter(
        x    = portfolio_df['timestamp'],
        y    = portfolio_df['total_value'],
        mode = 'lines+markers',
        name = 'Portfolio Value',
        line = dict(color=green, width=2),
        fill = 'tozeroy',
        fillcolor = 'rgba(46, 160, 67, 0.1)'
    ), row=1, col=1)

    fig.add_hline(
        y=initial_capital, line_dash='dot',
        line_color='rgba(255,255,255,0.3)',
        annotation_text=f'Start ${initial_capital}',
        row=1, col=1
    )

    # Panel 2: Cumulative PnL
    if 'cumulative_pnl' in portfolio_df.columns:
        pnl_vals  = pd.to_numeric(portfolio_df['cumulative_pnl'], errors='coerce')
        pnl_colors = [green if v >= 0 else red for v in pnl_vals]
        fig.add_trace(go.Scatter(
            x    = portfolio_df['timestamp'],
            y    = pnl_vals,
            mode = 'lines',
            name = 'Cumulative PnL',
            line = dict(color=blue, width=2),
        ), row=1, col=2)
        fig.add_hline(y=0, line_dash='dot',
                      line_color='rgba(255,255,255,0.3)', row=1, col=2)

    # Panel 3: Trade PnL distribution
    if not trades_df.empty and 'pnl' in trades_df.columns:
        sell_trades = trades_df[trades_df['action'] == 'SELL'].copy()
        if not sell_trades.empty:
            sell_trades['pnl'] = pd.to_numeric(sell_trades['pnl'], errors='coerce')
            bar_colors = [green if p >= 0 else red
                          for p in sell_trades['pnl']]
            fig.add_trace(go.Bar(
                x      = sell_trades['symbol'] + ' ' +
                          sell_trades['timestamp'].str[:10],
                y      = sell_trades['pnl'],
                name   = 'Trade PnL',
                marker_color = bar_colors,
            ), row=2, col=1)

    # Panel 4: Decision breakdown pie
    if not trades_df.empty:
        action_counts = trades_df['action'].value_counts()
        fig.add_trace(go.Pie(
            labels = action_counts.index.tolist(),
            values = action_counts.values.tolist(),
            name   = 'Decisions',
            marker = dict(colors=[green, red, blue, '#bc8cff']),
            hole   = 0.4,
        ), row=2, col=2)

    # Layout
    fig.update_layout(
        title       = dict(
            text     = f'Trading Agent Dashboard — Paper Session {session_id}',
            font     = dict(color=text_col, size=16)
        ),
        paper_bgcolor = dark_bg,
        plot_bgcolor  = panel_bg,
        font          = dict(color=text_col),
        height        = 700,
        showlegend    = False,
    )

    fig.update_xaxes(gridcolor='#21262d', showgrid=True)
    fig.update_yaxes(gridcolor='#21262d', showgrid=True)

    fig.show()

    # Print text summary
    total_value = float(portfolio_df['total_value'].iloc[-1]) \
                  if not portfolio_df.empty else initial_capital
    total_pnl   = total_value - initial_capital
    pnl_pct     = (total_pnl / initial_capital) * 100
    n_trades    = len(trades_df) if not trades_df.empty else 0

    print(f"\n{'='*55}")
    print(f"DASHBOARD SUMMARY — Session {session_id}")
    print(f"{'='*55}")
    print(f"  Portfolio value:  ${total_value:.4f}")
    print(f"  Total PnL:        ${total_pnl:+.4f} ({pnl_pct:+.2f}%)")
    print(f"  Total trades:     {n_trades}")
    print(f"  Data points:      {len(portfolio_df)} snapshots")
    print(f"{'='*55}")


