import gymnasium as gym
from gymnasium import spaces
import numpy as np
import pandas as pd

class TradingEnv(gym.Env):
    """
    Custom Gymnasium environment for single-asset trading.

    The agent trades one asset (e.g. BTC/USDT) using daily candles.
    It sees a window of technical indicators and decides to BUY, SELL, or HOLD.

    This is the training ground — real market data, simulated execution.
    """

    metadata = {'render_modes': ['human', 'silent']}

    def __init__(self, df, initial_capital=1000.0, window_size=20,
                 commission=0.001, render_mode='silent'):
        """
        Args:
            df:              OHLCV + indicators DataFrame (from add_indicators)
            initial_capital: Starting cash (we use $1000 for training, $20 for live)
            window_size:     How many past candles the agent sees at each step
            commission:      Trade fee as fraction (0.001 = 0.1% — KuCoin standard)
            render_mode:     'human' prints to console, 'silent' suppresses output
        """
        super().__init__()

        self.df              = df.reset_index(drop=True)
        self.initial_capital = initial_capital
        self.window_size     = window_size
        self.commission      = commission
        self.render_mode     = render_mode

        # ---------------------------------------------------------------
        # Feature columns — these are what the agent SEES each step
        # Normalised to [0, 1] range inside _get_observation()
        # ---------------------------------------------------------------
        self.feature_cols = [
            'close',         # Current price
            'volume',        # Trading volume
            'rsi_14',        # RSI momentum
            'macd',          # MACD line
            'macd_signal',   # MACD signal
            'macd_hist',     # MACD histogram
            'bb_pct',        # Bollinger Band position
            'ema_50',        # EMA 50 trend
            'ema_200',       # EMA 200 trend
            'atr_14',        # Volatility
            'volume_ratio',  # Volume anomaly
            'return_1d',     # Yesterday's return
        ]
        self.n_features = len(self.feature_cols)

        # ---------------------------------------------------------------
        # OBSERVATION SPACE
        # Shape: (window_size × n_features) + 3 account features
        # Account features: [cash_ratio, position_ratio, unrealised_pnl]
        # ---------------------------------------------------------------
        obs_size = (self.window_size * self.n_features) + 3
        self.observation_space = spaces.Box(
            low=-np.inf,
            high=np.inf,
            shape=(obs_size,),
            dtype=np.float32
        )

        # ---------------------------------------------------------------
        # ACTION SPACE
        # 0 = HOLD  (do nothing)
        # 1 = BUY   (spend 95% of available cash on current asset)
        # 2 = SELL  (sell entire position)
        # ---------------------------------------------------------------
        self.action_space = spaces.Discrete(3)

        # Validate data has required columns
        missing = [c for c in self.feature_cols if c not in df.columns]
        if missing:
            raise ValueError(f"DataFrame missing columns: {missing}")

        # Precompute min/max for normalisation (per column)
        self._feature_min = df[self.feature_cols].min()
        self._feature_max = df[self.feature_cols].max()
        self._feature_range = (self._feature_max - self._feature_min).replace(0, 1)


    def reset(self, seed=None, options=None):
        """
        Resets the environment to the start of an episode.
        Called at the beginning of each training episode.
        """
        super().reset(seed=seed)

        # Start after window_size so we have enough history for the first observation
        self.current_step = self.window_size

        # Account state
        self.cash         = self.initial_capital
        self.position     = 0.0      # How much of the asset we hold (units)
        self.entry_price  = 0.0      # Price we bought at
        self.total_value  = self.initial_capital

        # Episode tracking
        self.trades       = []
        self.portfolio_values = [self.initial_capital]
        self.returns      = []

        obs = self._get_observation()
        info = {}
        return obs, info


    def step(self, action):
        """
        Execute one timestep — agent takes action, environment responds.

        Returns:
            observation: What the agent sees next
            reward:      How good the action was
            terminated:  True if episode is done (end of data)
            truncated:   True if episode cut short (not used here)
            info:        Debug information dictionary
        """
        current_price = float(self.df.loc[self.current_step, 'close'])
        prev_value    = self._portfolio_value(current_price)

        # ---------------------------------------------------------------
        # Execute action
        # ---------------------------------------------------------------
        trade_executed = False
        trade_cost     = 0.0

        if action == 1:  # BUY
            if self.cash > 1.0 and self.position == 0:
                # Spend 95% of cash (keep 5% reserve for fees)
                spend         = self.cash * 0.95
                trade_cost    = spend * self.commission
                units_bought  = (spend - trade_cost) / current_price
                self.position    = units_bought
                self.entry_price = current_price
                self.cash       -= spend
                trade_executed   = True
                self.trades.append({
                    'step': self.current_step, 'action': 'BUY',
                    'price': current_price, 'units': units_bought
                })

        elif action == 2:  # SELL
            if self.position > 0:
                proceeds      = self.position * current_price
                trade_cost    = proceeds * self.commission
                self.cash    += (proceeds - trade_cost)
                pnl           = (current_price - self.entry_price) * self.position
                self.trades.append({
                    'step': self.current_step, 'action': 'SELL',
                    'price': current_price, 'pnl': pnl
                })
                self.position  = 0.0
                self.entry_price = 0.0
                trade_executed = True

        # Advance to next candle
        self.current_step += 1

        # ---------------------------------------------------------------
        # Calculate reward
        # ---------------------------------------------------------------
        new_price  = float(self.df.loc[self.current_step, 'close']) \
                     if self.current_step < len(self.df) else current_price
        new_value  = self._portfolio_value(new_price)
        step_return = (new_value - prev_value) / (prev_value + 1e-8)

        self.portfolio_values.append(new_value)
        self.returns.append(step_return)

        reward = self._calculate_reward(
            step_return=step_return,
            trade_cost=trade_cost,
            action=action
        )

        # ---------------------------------------------------------------
        # Check if episode is done
        # ---------------------------------------------------------------
        terminated = self.current_step >= len(self.df) - 1

        obs  = self._get_observation()
        info = {
            'step':           self.current_step,
            'portfolio_value': new_value,
            'cash':           self.cash,
            'position':       self.position,
            'price':          new_price,
            'return':         step_return,
            'n_trades':       len(self.trades),
        }

        if self.render_mode == 'human':
            self.render()

        return obs, reward, terminated, False, info


    def _get_observation(self):
        """
        Returns the agent's observation at the current step.

        Structure:
        - Normalised feature values for the last window_size candles
        - Account state: [cash_ratio, position_ratio, unrealised_pnl_pct]
        """
        # Window of historical data
        start = self.current_step - self.window_size
        end   = self.current_step
        window = self.df.loc[start:end-1, self.feature_cols].copy()

        # Normalise to [-1, 1] range (helps RL training stability)
        normalised = (window - self._feature_min) / self._feature_range
        normalised = normalised.fillna(0).values.flatten().astype(np.float32)

        # Account state features
        current_price = float(self.df.loc[self.current_step, 'close'])
        total_value   = self._portfolio_value(current_price)

        cash_ratio     = self.cash / (total_value + 1e-8)
        position_ratio = (self.position * current_price) / (total_value + 1e-8)
        unrealised_pnl = (current_price - self.entry_price) / (self.entry_price + 1e-8) \
                         if self.position > 0 else 0.0

        account_state = np.array(
            [cash_ratio, position_ratio, unrealised_pnl],
            dtype=np.float32
        )

        return np.concatenate([normalised, account_state])


    def _portfolio_value(self, price):
        """Total portfolio value = cash + position market value."""
        return self.cash + (self.position * price)


    def _calculate_reward(self, step_return, trade_cost, action):
        """
        Reward function — the most important design decision in RL trading.

        Components:
        1. Step return     : reward profit, penalise loss
        2. Sharpe bonus    : reward consistent returns (not just big swings)
        3. Trade cost pen  : penalise excessive trading (commission drag)
        4. Inaction pen    : small penalty for holding cash too long
        5. Drawdown pen    : penalise large drawdowns from peak

        Why Sharpe-based: raw PnL reward makes agents take huge reckless
        bets. Sharpe ratio rewards CONSISTENT returns — exactly what a
        real trader wants.
        """
        reward = step_return * 100  # Scale up for training signal clarity

        # Sharpe bonus: reward consistency over 20-step rolling window
        if len(self.returns) >= 20:
            recent_returns = np.array(self.returns[-20:])
            mean_r  = np.mean(recent_returns)
            std_r   = np.std(recent_returns) + 1e-8
            sharpe  = mean_r / std_r
            reward += sharpe * 0.1   # Add Sharpe component

        # Commission drag penalty
        reward -= trade_cost * 10

        # Inaction penalty: penalise holding full cash for too long
        # (encourages agent to find trades, not just sit out)
        if action == 0 and self.position == 0:
            reward -= 0.001

        # Drawdown penalty: penalise losses from portfolio peak
        if len(self.portfolio_values) > 1:
            peak     = max(self.portfolio_values)
            current  = self.portfolio_values[-1]
            drawdown = (peak - current) / (peak + 1e-8)
            if drawdown > 0.1:   # Only penalise drawdowns > 10%
                reward -= drawdown * 5

        return float(reward)


    def render(self):
        """Print current state to console (for debugging)."""
        if self.current_step < len(self.df):
            price = float(self.df.loc[self.current_step, 'close'])
            value = self._portfolio_value(price)
            pnl   = ((value - self.initial_capital) / self.initial_capital) * 100
            print(f"Step {self.current_step:>4} | "
                  f"Price: ${price:>10,.2f} | "
                  f"Value: ${value:>10,.2f} | "
                  f"PnL: {pnl:>+7.2f}% | "
                  f"Trades: {len(self.trades)}")


    def get_episode_stats(self):
        """
        Returns performance statistics for the completed episode.
        Call this after an episode finishes (terminated=True).
        """
        if len(self.portfolio_values) < 2:
            return {}

        values  = np.array(self.portfolio_values)
        returns = np.array(self.returns)

        total_return = (values[-1] - values[0]) / values[0] * 100
        peak         = np.maximum.accumulate(values)
        drawdowns    = (peak - values) / (peak + 1e-8)
        max_drawdown = float(np.max(drawdowns)) * 100

        sharpe = 0.0
        if len(returns) > 1 and np.std(returns) > 0:
            sharpe = float(np.mean(returns) / np.std(returns) * np.sqrt(252))

        buy_trades  = [t for t in self.trades if t['action'] == 'BUY']
        sell_trades = [t for t in self.trades if t['action'] == 'SELL']
        pnl_trades  = [t.get('pnl', 0) for t in sell_trades]
        win_rate    = (sum(1 for p in pnl_trades if p > 0) / len(pnl_trades) * 100) \
                      if pnl_trades else 0

        return {
            'total_return_pct':  round(total_return, 2),
            'final_value':       round(float(values[-1]), 2),
            'max_drawdown_pct':  round(max_drawdown, 2),
            'sharpe_ratio':      round(sharpe, 3),
            'n_trades':          len(self.trades),
            'win_rate_pct':      round(win_rate, 2),
            'n_buy':             len(buy_trades),
            'n_sell':            len(sell_trades),
        }

