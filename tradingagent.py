# CELL 9 — Backtest Framework
#
# A backtest simulates the trained agent on historical data and measures
# performance as if it were trading in real markets.
#
# Key metrics we track:
#   Total Return %    — did we make money overall?
#   Sharpe Ratio      — return per unit of risk (> 1.5 = good, > 2.0 = great)
#   Max Drawdown %    — worst peak-to-trough loss (< 20% = acceptable)
#   Win Rate %        — % of profitable trades
#   Profit Factor     — gross profit / gross loss (> 1.5 = good)
#   Calmar Ratio      — annual return / max drawdown (> 1.0 = good)
#
# Benchmark: Buy & Hold BTC for the same period
# Your agent must BEAT buy & hold to be worth running.
# =============================================================================

from stable_baselines3 import PPO
from stable_baselines3.common.vec_env import DummyVecEnv, VecNormalize

def run_backtest(model, df, initial_capital=1000.0,
                 label='Agent', vec_normalize_path=None):
    """
    Runs a full backtest of a trained model on a DataFrame.

    Returns a dict of performance metrics + a DataFrame of
    per-step portfolio values for charting.
    """
    # Create single eval environment
    eval_env = DummyVecEnv([make_env(df, initial_capital=initial_capital)])

    # Load normalisation if provided (must match training normalisation)
    if vec_normalize_path:
        eval_env = VecNormalize.load(vec_normalize_path, eval_env)
        eval_env.training    = False   # Freeze normalisation stats
        eval_env.norm_reward = False   # Don't normalise rewards during eval

    obs   = eval_env.reset()
    done  = False

    portfolio_values = [initial_capital]
    actions_taken    = []
    prices           = []
    step             = 0

    while not done:
        # Deterministic = True: agent uses best known action, no exploration
        action, _ = model.predict(obs, deterministic=True)
        obs, reward, done_arr, info_arr = eval_env.step(action)
        done = done_arr[0]

        info = info_arr[0]
        portfolio_values.append(info.get('portfolio_value', portfolio_values[-1]))
        actions_taken.append(int(action[0]))
        prices.append(info.get('price', 0))
        step += 1

    # Compute metrics
    values  = np.array(portfolio_values)
    returns = np.diff(values) / (values[:-1] + 1e-8)

    total_return = (values[-1] - values[0]) / values[0] * 100

    # Sharpe ratio (annualised, assuming daily data)
    sharpe = 0.0
    if len(returns) > 1 and np.std(returns) > 0:
        sharpe = float(np.mean(returns) / np.std(returns) * np.sqrt(252))

    # Max drawdown
    peak     = np.maximum.accumulate(values)
    drawdown = (peak - values) / (peak + 1e-8)
    max_dd   = float(np.max(drawdown)) * 100

    # Calmar ratio
    annual_return = total_return * (252 / len(returns))
    calmar = annual_return / (max_dd + 1e-8)

    # Trade stats
    buys      = actions_taken.count(1)
    sells     = actions_taken.count(2)
    holds     = actions_taken.count(0)

    metrics = {
        'label':            label,
        'initial_capital':  initial_capital,
        'final_value':      round(float(values[-1]), 2),
        'total_return_pct': round(total_return, 2),
        'sharpe_ratio':     round(sharpe, 3),
        'max_drawdown_pct': round(max_dd, 2),
        'calmar_ratio':     round(calmar, 3),
        'n_steps':          len(returns),
        'n_buys':           buys,
        'n_sells':          sells,
        'n_holds':          holds,
        'trade_frequency':  round((buys + sells) / max(len(actions_taken), 1) * 100, 1),
    }

    portfolio_df = pd.DataFrame({
        'step':            range(len(portfolio_values)),
        'portfolio_value': portfolio_values,
        'action':          [-1] + actions_taken,   # -1 for initial step
        'price':           [prices[0] if prices else 0] + prices
    })

    return metrics, portfolio_df


def run_buy_hold_benchmark(df, initial_capital=1000.0):
    """
    Buy and Hold benchmark — buys at first candle, holds to last.
    Your agent must beat this. If it can't, just buy and hold is better.
    """
    prices = df['close'].values
    if len(prices) < 2:
        return {}, pd.DataFrame()

    units    = initial_capital / prices[0]
    values   = units * prices
    returns  = np.diff(values) / (values[:-1] + 1e-8)

    total_return = (values[-1] - values[0]) / values[0] * 100
    sharpe = float(np.mean(returns) / np.std(returns) * np.sqrt(252)) \
             if np.std(returns) > 0 else 0.0

    peak   = np.maximum.accumulate(values)
