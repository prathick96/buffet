def load_ohlcv(symbol, start_date=None, end_date=None, source='supabase'):
    """
    Universal data loader — fetches from Supabase or falls back to CSV.
    This is the function your RL training pipeline will call.

    Args:
        symbol:     e.g. 'BTC/USDT'
        start_date: '2022-01-01' (optional)
        end_date:   '2024-12-31' (optional)
        source:     'supabase' or 'csv'

    Returns:
        Pandas DataFrame with OHLCV columns, sorted by timestamp
    """

    query = supabase_client.table('ohlcv_data')\
          .select('timestamp,symbol,exchange,timeframe,open,high,low,close,volume')\
          .eq('symbol', symbol).order('timestamp', desc=False).limit(10000)
    if start_date:
        query = query.gte('timestamp', f'{start_date}T00:00:00+00:00')  # ← add timezone
    if end_date:
        query = query.lte('timestamp', f'{end_date}T23:59:59+00:00')    # ← add timezone
    result = query.execute()
    df = pd.DataFrame(result.data)
    if df.empty:
        return df


    elif source == 'csv':
        safe_name = symbol.replace('/', '_')
        path = f'trading_data/historical/{safe_name}_1d_5yr.csv'
        df = pd.read_csv(path, parse_dates=['timestamp'])
        if start_date:
            df = df[df['timestamp'] >= start_date]
        if end_date:
            df = df[df['timestamp'] <= end_date]
        return df.reset_index(drop=True)
    df['timestamp'] = pd.to_datetime(df['timestamp'], utc=True)
    return df

