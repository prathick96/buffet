# DAY 5 — CELL 7: Technical Indicators with pandas-ta
#
# Technical indicators are mathematical signals computed from OHLCV data.
# Your RL agent's observation space will include these as input features.
#
# The 3 most important indicators to start:
#   RSI   — momentum (is asset overbought or oversold?)
#   MACD  — trend direction and strength
#   BB    — Bollinger Bands (volatility + price extremes)
# =============================================================================

import pandas_ta as ta

def add_indicators(df):
    """
    Adds technical indicators to an OHLCV DataFrame.
    These become the feature vector your RL agent observes.

    Modifies df in-place, returns df with new columns.
    """
    # Make a clean copy to avoid modifying original
    df = df.copy()

    # Ensure correct column types
    for col in ['open','high','low','close','volume']:
        df[col] = pd.to_numeric(df[col], errors='coerce')

    # --- RSI: Relative Strength Index (14-period standard) ---
    # Range: 0-100
    # < 30 = oversold (potential buy signal)
    # > 70 = overbought (potential sell signal)
    df['rsi_14'] = ta.rsi(df['close'], length=14)

    # --- MACD: Moving Average Convergence Divergence ---
    # Returns 3 columns: MACD line, Signal line, Histogram
    # MACD crosses above signal → bullish
    # MACD crosses below signal → bearish
    # MACD — dynamic column names
    macd = ta.macd(df['close'], fast=12, slow=26, signal=9)
    macd_cols   = macd.columns.tolist()
    macd_line   = [c for c in macd_cols if c.startswith('MACD_')][0]
    macd_signal = [c for c in macd_cols if c.startswith('MACDs_')][0]
    macd_hist   = [c for c in macd_cols if c.startswith('MACDh_')][0]

    df['macd']        = macd[macd_line]
    df['macd_signal'] = macd[macd_signal]
    df['macd_hist']   = macd[macd_hist]

    # --- Bollinger Bands (20-period, 2 std deviations) ---
    # Upper band = price is high relative to recent history
    # Lower band = price is low relative to recent history
    # %B tells you where price is within the bands (0=lower, 1=upper)
    # Bollinger Bands
    bb = ta.bbands(df['close'], length=20, std=2)
    bb_cols    = bb.columns.tolist()
    upper_col  = [c for c in bb_cols if c.startswith('BBU')][0]
    middle_col = [c for c in bb_cols if c.startswith('BBM')][0]
    lower_col  = [c for c in bb_cols if c.startswith('BBL')][0]
    pct_col    = [c for c in bb_cols if c.startswith('BBP')][0]

    df['bb_upper']  = bb[upper_col]   # ← uses variable, not hardcoded string
    df['bb_middle'] = bb[middle_col]
    df['bb_lower']  = bb[lower_col]
    df['bb_pct']    = bb[pct_col]

    # --- Additional useful features for RL ---

    # EMA 50 and EMA 200 (trend direction)
    df['ema_50']  = ta.ema(df['close'], length=50)
    df['ema_200'] = ta.ema(df['close'], length=200)

    # ATR: Average True Range (volatility measure — used for position sizing)
    df['atr_14'] = ta.atr(df['high'], df['low'], df['close'], length=14)

    # Volume moving average (is today's volume unusual?)
    df['volume_sma_20'] = ta.sma(df['volume'], length=20)
    df['volume_ratio']  = df['volume'] / df['volume_sma_20']

    # Daily return %
    df['return_1d'] = df['close'].pct_change() * 100

    # Drop rows where indicators couldn't be computed (first N rows)
    df = df.dropna().reset_index(drop=True)

    return df


