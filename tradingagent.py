class SecurityAgent:
    """
    The master security layer that wraps all exchange interactions.

    Your trading code should NEVER call exchange APIs directly.
    Always go through SecurityAgent — it handles:
    - API key retrieval (encrypted)
    - Rate limiting (ban prevention)
    - Response validation (data integrity)
    - Circuit breaking (loss protection)
    - Audit logging (tamper-evident trail)
    """

    def __init__(self, master_password: str, initial_portfolio_value: float = 20.0):
        print("[SECURITY] Initialising Security Agent...")

        self.key_store  = SecureKeyStore(master_password=master_password)
        self.validators = {}   # symbol → ResponseValidator
        self.breaker    = CircuitBreaker(
            daily_loss_limit_pct=5.0,     # Stop if down 5% in a day
            weekly_loss_limit_pct=15.0,   # Stop if down 15% in a week
            max_consecutive_losses=5,
            volatility_halt_pct=15.0      # Halt on 15% price move
        )
        self.rate_limiters = {}   # exchange → RateLimitManager
        self._audit_log    = []
        self._portfolio_value = initial_portfolio_value

        self.breaker.set_session_start(initial_portfolio_value)
        print("[SECURITY] Security Agent ready")

    def get_rate_limiter(self, exchange: str) -> RateLimitManager:
        """Get or create rate limiter for an exchange."""
        if exchange not in self.rate_limiters:
            self.rate_limiters[exchange] = RateLimitManager(exchange)
        return self.rate_limiters[exchange]

    def get_validator(self, symbol: str) -> ResponseValidator:
        """Get or create response validator for a symbol."""
        if symbol not in self.validators:
            self.validators[symbol] = ResponseValidator()
        return self.validators[symbol]

    def safe_fetch_ticker(self, exchange_obj, symbol: str) -> dict | None:
        """
        Safely fetches a ticker with rate limiting + validation.
        Returns None if data is invalid or rate limit hit.
        """
        exchange_name = exchange_obj.name.lower()
        limiter       = self.get_rate_limiter(exchange_name)
        validator     = self.get_validator(symbol)

        try:
            limiter.wait_if_needed('public')
            ticker = exchange_obj.fetch_ticker(symbol)

            if not validator.validate_ticker(symbol, ticker):
                self._audit('REJECTED_TICKER', symbol, {'reason': 'validation_failed'})
                return None

            self._audit('TICKER_OK', symbol, {'price': ticker['last']})
            return ticker

        except Exception as e:
            error_str = str(e).lower()
            if '429' in error_str or 'rate limit' in error_str:
                limiter.report_rate_limit_error('public')
            self._audit('TICKER_ERROR', symbol, {'error': str(e)})
            return None

    def can_trade(self, current_portfolio_value: float,
                  current_price: float = None,
                  prev_price: float    = None) -> bool:
        """
        Master trading gate — returns True only if ALL checks pass.
        Call this before every order.
        """
        allowed = self.breaker.check(
            current_portfolio_value=current_portfolio_value,
            current_price=current_price,
            prev_price=prev_price
        )

        if not allowed:
            self._audit('TRADE_BLOCKED', 'SYSTEM',
                        {'reason': self.breaker.status['reason'],
                         'portfolio_value': current_portfolio_value})

        return allowed

    def record_trade(self, symbol: str, action: str, price: float,
                     pnl: float = None):
        """Record a completed trade for circuit breaker tracking."""
        if pnl is not None:
            self.breaker.record_trade_result(pnl)
        self._audit('TRADE_EXECUTED', symbol,
                    {'action': action, 'price': price, 'pnl': pnl})

    def _audit(self, event_type: str, symbol: str, details: dict):
        """Write to tamper-evident audit log."""
        entry = {
            'timestamp':  datetime.utcnow().isoformat(),
            'event':      event_type,
            'symbol':     symbol,
            'details':    details,
        }
        self._audit_log.append(entry)

        # For important events, also print to console
        if event_type in ('TRADE_BLOCKED', 'TRADE_EXECUTED', 'REJECTED_TICKER'):
            print(f"[AUDIT] {entry['timestamp']} | {event_type} | {symbol} | {details}")

    def get_security_report(self):
        """Full security status report."""
        print("\n" + "="*60)
        print("SECURITY AGENT STATUS REPORT")
        print("="*60)
        print(f"Circuit Breaker:  {'TRIPPED' if self.breaker.is_tripped else 'OK'}")
        if self.breaker.is_tripped:
            print(f"  Reason: {self.breaker.status['reason']}")
        print(f"Consecutive losses: {self.breaker.status['consecutive_losses']}")
        print(f"Audit log entries:  {len(self._audit_log)}")

        for exc, limiter in self.rate_limiters.items():
            stats = limiter.get_request_stats()
            print(f"Rate limiter [{exc}]: {stats}")

        anomalies = {}
        for sym, validator in self.validators.items():
            a = validator.get_anomaly_summary()
            if a:
                anomalies[sym] = a
        if anomalies:
            print(f"Data anomalies detected: {anomalies}")
        else:
            print("Data anomalies: none")
        print("="*60)


